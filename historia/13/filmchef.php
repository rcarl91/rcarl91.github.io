<h1>Pablo</h1>
  <p>Hejsan!

Mitt namn är Pablo och jag är filmchef i EKAK, det innebär att jag är ansvarig för en större del av EKAKs uppgifter, dvs visa Kalle film. Trycket på min rygg är ifrån filmen ”The Three Caballeros” som kom ut 1945 där Pablo är med i första och viktigaste delen utav filmen.
<br><br>
Pablo är en kallblodig äventyrare som gör allt för att nå sitt mål,  Han korsar hav, seglar genom storm och dimma, han möter även Poseidon på sitt äventyr. Pablos bästa vän och trogne kompanjon Smokey Joe är hans ända sällskap under resan, som tyvärr inte klarar sig igenom den påfrestande färden.
