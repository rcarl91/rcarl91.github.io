<p>Glittriga Gullan såg ljuset första gången år 1953 då hon skapades av
mästaren Carl Barks. Han använde henne endast i en enda serie (Tillbaka
till klondike). Därefter har hon flitigt och ofta dykt upp i serier av
andra skickliga tecknare och även i avsnitt av Duck Tales. Gullan är nog
mest känd för att vara Farbror Joakims stora ungdomskärlek. De träffades
då Joakim grävde guld i Klondike år 1899. Där lät han sig förtjusas och
förledas av den unga  och vackra ballroomsdansösen som  drogade Farbror
Joakim och sedan bestal honom på en stor guldklimp.</p>

